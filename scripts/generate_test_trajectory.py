#! /usr/bin/env python3

import rospy
import numpy as np

n_layers = 3
n_points = 10
radius = 1.0
layer_height = 0.3
theta_inc = 2 * np.pi / n_points

f = open('../cfg/cylinder_points.yaml', 'w')
f.write('#DO_NOT_MODIFY: This config file was autogenerated! \n\n')
f.write('waypoints:\n\n')
f.write('  n_layers: '+str(n_layers)+'\n\n')
for i in range(n_layers):
    f.write('  layer'+str(i)+':\n')
    z = layer_height * i
    for j in range(n_points+1):
        theta = theta_inc * j
        x = radius * np.sin(theta)
        y = radius * np.cos(theta)
        pose = [x, y, z, 0.0, 0.0, 0.0]
        f.write('    point'+str(j)+': '+str(pose)+'\n')
    f.write('\n')
